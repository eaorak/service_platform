package com.adenon.sp.streams;

import javax.persistence.Entity;
import javax.persistence.Id;

@Entity(name = "streams")
public class Streams {

    private String id;

    public Streams() {
    }

    public Streams(final String id) {
        this.id = id;
    }

    @Id
    public String getId() {
        return this.id;
    }

    public void setId(final String id) {
        this.id = id;
    }

}
