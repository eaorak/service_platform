package com.adenon.sp.services;

import javax.persistence.Entity;
import javax.persistence.Id;

@Entity(name = "hubele")
public class Hubele {

    private String id;

    public Hubele() {
    }

    public Hubele(final String id) {
        this.id = id;
    }

    @Id
    public String getId() {
        return this.id;
    }

    public void setId(final String id) {
        this.id = id;
    }

}
